"""
5 - Faça a leitura de três valores e apresente como resultado a
soma dos quadrados dos três valores lidos.
"""

print('Digite 3 números para vermos sua soma dos seus quadrados:')
num1 = int(input('Número 1: '))
num2 = int(input('Número 2: '))
num3 = int(input('Número 3: '))
soma = num1**2 + num2**2 + num3**2

print(f'A soma deles é {soma}')
