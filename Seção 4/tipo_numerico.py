"""
Tipo Numérico
"""

# Tipo básico
num = 5
print(type(num))
print(num)

# Operações
print(num + 2)
print(num - 2)
print(num * 2)
print(num / 2)  # Divisão normal
print(num // 2)  # Divisão inteira
print(num % 2)  # Resto da divisão
print(num ** 2)  # Elevado à

# Facilidades
um_milhao = 1000000
dez_milhoes = 10000000
um_bilhao = 1000000000

um_milhao_bonito = 1_000_000
dez_milhoes_bonito = 10_000_000
um_bilhao_bonito = 1_000_000_000

print(um_milhao)
print(um_milhao_bonito)
print(dez_milhoes)
print(dez_milhoes_bonito)
print(um_bilhao)
print(um_bilhao_bonito)

# Incremento e Decremento
num += 1
print(num)
num -= 1
print(num)
